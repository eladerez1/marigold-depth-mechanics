"""
UVeye Pi3 depth dataset.

For each inspection session Pi3 outputs:
  - inputs/images/<cam_name>/frame_XXXX.png     — RGB images (1544×2064)
  - inputs/calibration.yaml                     — pinhole intrinsics + T_cn_cnm1 chain
  - 02_trajectory/trajectory.npz                — pose_cache_{cameras,frames,values}
  - 03_reconstruction/frame_XXXX_caronly.ply    — car-surface point cloud per frame

This dataset iterates over (session, camera, frame) triples and returns:
  - rgb  : torch.float32 [3, H, W] in [0, 1]
  - depth: torch.float32 [1, H, W]  – sparse, 0 = invalid; metric depth in metres
  - mask : torch.bool    [1, H, W]  – True where depth is valid

The depth map is generated by projecting the frame_XXXX_caronly.ply points into each
camera view using the stored calibration and trajectory poses.

Coordinate convention:
  pose_cache_values[i] is the 4×4 world-to-camera transform (i.e. transforms a 3-D
  point in the reconstruction's world frame into camera coordinates where z > 0 is
  in front of the camera). This is validated at init time for one sample.
"""

from __future__ import annotations

import logging
from pathlib import Path
from typing import Optional

import cv2
import numpy as np
import torch
import yaml
from torch.utils.data import Dataset

log = logging.getLogger(__name__)


# ---------------------------------------------------------------------------
# Calibration helpers
# ---------------------------------------------------------------------------

def _parse_calibration(calib_path: Path) -> dict[str, dict]:
    """Return dict {cam_name: {K: 3×3, dist: 4-vec, res: (W, H)}}."""
    with open(calib_path) as f:
        raw = yaml.safe_load(f)

    # Build chain transforms: each cam has T_cn_cnm1 (cam_n to cam_{n-1})
    # We accumulate to get T_c0_cn (cam_0 to cam_n), but we only need per-camera K.
    cams: dict[str, dict] = {}
    for key, v in raw.items():
        if not key.startswith("cam") or not isinstance(v, dict):
            continue
        name = v.get("cam_name")
        if name is None:
            continue
        fx, fy, cx, cy = v["intrinsics"]
        K = np.array([[fx, 0, cx], [0, fy, cy], [0, 0, 1]], dtype=np.float32)
        dist = np.array(v["distortion_coeffs"], dtype=np.float32)
        W, H = v["resolution"]
        cams[name] = {"K": K, "dist": dist, "W": W, "H": H}
    return cams


# ---------------------------------------------------------------------------
# PLY loader (minimal — only reads XYZ vertices)
# ---------------------------------------------------------------------------

def _load_ply_xyz(ply_path: Path) -> np.ndarray:
    """Return (N, 3) float32 XYZ array from an ASCII or binary PLY."""
    import struct

    with open(ply_path, "rb") as f:
        header_lines: list[bytes] = []
        while True:
            line = f.readline()
            header_lines.append(line)
            if line.strip() == b"end_header":
                break

        header = b"".join(header_lines).decode("utf-8", errors="replace")
        n_vertex = 0
        for ln in header.splitlines():
            if ln.startswith("element vertex"):
                n_vertex = int(ln.split()[-1])
                break

        is_binary_le = "format binary_little_endian" in header
        is_binary_be = "format binary_big_endian" in header

        # Count float/double properties to know stride
        props = []
        in_vertex = False
        for ln in header.splitlines():
            if ln.startswith("element vertex"):
                in_vertex = True
            elif ln.startswith("element") and in_vertex:
                break
            elif in_vertex and ln.startswith("property"):
                parts = ln.split()
                props.append(parts[1])  # type name

        if is_binary_le or is_binary_be:
            fmt_char = "<" if is_binary_le else ">"
            type_map = {"float": "f", "float32": "f", "double": "d",
                        "float64": "d", "int": "i", "uint": "I",
                        "uchar": "B", "short": "h"}
            fmt = fmt_char + "".join(type_map.get(p, "f") for p in props)
            stride = struct.calcsize(fmt)
            raw = f.read(n_vertex * stride)
            pts = np.frombuffer(raw, dtype=np.dtype(fmt.replace("<", "").replace(">", "")))
            pts = pts.reshape(n_vertex, -1)
            xyz = pts[:, :3].astype(np.float32)
        else:
            # ASCII
            lines = f.read().decode("utf-8", errors="replace").splitlines()
            rows = [list(map(float, ln.split()[:3])) for ln in lines[:n_vertex] if ln.strip()]
            xyz = np.array(rows, dtype=np.float32)

    return xyz


# ---------------------------------------------------------------------------
# Depth map generation
# ---------------------------------------------------------------------------

def _project_to_depth(
    xyz_world: np.ndarray,   # (N, 3)
    pose_w2c: np.ndarray,    # (4, 4) world-to-camera
    K: np.ndarray,            # (3, 3)
    dist: np.ndarray,         # (4,) or (5,)
    W: int,
    H: int,
    depth_scale: float = 1.0,
) -> np.ndarray:
    """
    Project world-frame points into a camera and return a dense depth image.

    Returns float32 array (H, W) where 0 means no observation.
    Uses nearest-depth win for overlapping points.
    """
    N = len(xyz_world)
    if N == 0:
        return np.zeros((H, W), dtype=np.float32)

    # Transform to camera coordinates
    xyz_h = np.concatenate([xyz_world, np.ones((N, 1), dtype=np.float32)], axis=1)  # (N,4)
    xyz_cam = (pose_w2c @ xyz_h.T).T  # (N, 4)
    z = xyz_cam[:, 2]  # depth

    # Keep only points in front of the camera
    valid = z > 0.01
    if valid.sum() == 0:
        return np.zeros((H, W), dtype=np.float32)

    xyz_cam = xyz_cam[valid]
    z = z[valid]

    # Project using OpenCV (handles distortion)
    rvec = np.zeros(3, dtype=np.float32)
    tvec = np.zeros(3, dtype=np.float32)
    pts3d = xyz_cam[:, :3].reshape(-1, 1, 3)
    pts2d, _ = cv2.projectPoints(pts3d, rvec, tvec, K, dist)
    pts2d = pts2d.reshape(-1, 2)

    # Build sparse depth map (nearest wins)
    depth_img = np.zeros((H, W), dtype=np.float32)
    xs = np.round(pts2d[:, 0]).astype(int)
    ys = np.round(pts2d[:, 1]).astype(int)
    in_bounds = (xs >= 0) & (xs < W) & (ys >= 0) & (ys < H)
    xs, ys, zs = xs[in_bounds], ys[in_bounds], z[in_bounds]

    # Process from back to front so nearest wins
    order = np.argsort(zs)[::-1]
    depth_img[ys[order], xs[order]] = zs[order] * depth_scale

    return depth_img


# ---------------------------------------------------------------------------
# Sample discovery
# ---------------------------------------------------------------------------

def _discover_samples(sessions_root: Path) -> list[dict]:
    """
    Walk sessions_root/<session_id>/run_*/  and return one dict per
    (session, run, cam, frame) quad that has all required files.
    """
    samples = []
    for session_dir in sorted(sessions_root.iterdir()):
        if not session_dir.is_dir():
            continue
        # Find the first run that has 03_reconstruction with caronly PLY files
        for run_dir in sorted(session_dir.iterdir()):
            if not run_dir.is_dir():
                continue
            recon_dir = run_dir / "03_reconstruction"
            traj_file = run_dir / "02_trajectory" / "trajectory.npz"
            calib_file = run_dir / "inputs" / "calibration.yaml"
            img_root = run_dir / "inputs" / "images"

            if not (recon_dir.exists() and traj_file.exists() and
                    calib_file.exists() and img_root.exists()):
                continue

            ply_files = sorted(recon_dir.glob("frame_*_caronly.ply"))
            if not ply_files:
                continue

            # Load trajectory to discover (cam, frame) pairs
            try:
                traj = np.load(traj_file, allow_pickle=False)
                pose_cameras = traj["pose_cache_cameras"]   # (M,) str
                pose_frames = traj["pose_cache_frames"]     # (M,) int
                pose_values = traj["pose_cache_values"]     # (M, 4, 4)
            except Exception as e:
                log.warning("Cannot load trajectory %s: %s", traj_file, e)
                continue

            for idx in range(len(pose_cameras)):
                cam_name = str(pose_cameras[idx])
                frame_idx = int(pose_frames[idx])
                pose = pose_values[idx]

                img_path = img_root / cam_name / f"frame_{frame_idx:04d}.png"
                ply_path = recon_dir / f"frame_{frame_idx:04d}_caronly.ply"

                if not img_path.exists() or not ply_path.exists():
                    continue

                samples.append({
                    "session": session_dir.name,
                    "run": run_dir.name,
                    "cam": cam_name,
                    "frame": frame_idx,
                    "img_path": img_path,
                    "ply_path": ply_path,
                    "pose_w2c": pose,
                    "calib_path": calib_file,
                })

            break  # Use first valid run per session

    log.info("Discovered %d (session, cam, frame) samples from %s", len(samples), sessions_root)
    return samples


# ---------------------------------------------------------------------------
# Dataset
# ---------------------------------------------------------------------------

class UVeyePi3Dataset(Dataset):
    """
    Torch Dataset over UVeye Pi3 inspection sessions.

    Args:
        sessions_root: Path to the root containing per-session directories,
                       e.g. /isilon/Automotive/RnD/elad.e/uv-3d/sessions/pi3_benchmark
        image_size: (H, W) to resize RGB and depth maps to. Default (480, 640).
        min_valid_frac: Minimum fraction of image pixels that must have valid depth
                        for a sample to be included. Default 0.01 (1%).
        max_samples: Cap on total samples (useful for debugging).
    """

    SESSIONS_ROOT = Path(
        "/isilon/Automotive/RnD/elad.e/uv-3d/sessions/pi3_benchmark"
    )

    def __init__(
        self,
        sessions_root: Optional[Path] = None,
        image_size: tuple[int, int] = (480, 640),
        min_valid_frac: float = 0.01,
        max_samples: Optional[int] = None,
    ):
        self.sessions_root = Path(sessions_root or self.SESSIONS_ROOT)
        self.image_size = image_size  # (H, W)
        self.min_valid_frac = min_valid_frac

        raw_samples = _discover_samples(self.sessions_root)

        # Load and cache calibrations (one per calib_path)
        self._calib_cache: dict[str, dict] = {}
        for s in raw_samples:
            key = str(s["calib_path"])
            if key not in self._calib_cache:
                try:
                    self._calib_cache[key] = _parse_calibration(s["calib_path"])
                except Exception as e:
                    log.warning("Cannot parse calibration %s: %s", s["calib_path"], e)

        # Filter to samples whose calibration loaded and cam_name is in it
        self.samples = []
        for s in raw_samples:
            key = str(s["calib_path"])
            calib = self._calib_cache.get(key, {})
            if s["cam"] in calib:
                self.samples.append(s)

        if max_samples is not None:
            self.samples = self.samples[:max_samples]

        log.info("UVeyePi3Dataset: %d samples (after calib filter)", len(self.samples))

    def __len__(self) -> int:
        return len(self.samples)

    def __getitem__(self, idx: int) -> dict:
        s = self.samples[idx]
        H, W = self.image_size

        # --- RGB ---
        img_bgr = cv2.imread(str(s["img_path"]))
        if img_bgr is None:
            raise FileNotFoundError(f"Image not found: {s['img_path']}")
        img_rgb = cv2.cvtColor(img_bgr, cv2.COLOR_BGR2RGB)
        img_rgb = cv2.resize(img_rgb, (W, H), interpolation=cv2.INTER_AREA)
        rgb = torch.from_numpy(img_rgb).permute(2, 0, 1).float() / 255.0  # [3,H,W]

        # --- Calibration ---
        calib = self._calib_cache[str(s["calib_path"])][s["cam"]]
        K_orig = calib["K"].copy()
        W_orig, H_orig = calib["W"], calib["H"]
        dist = calib["dist"]

        # Scale intrinsics to resized resolution
        K_scaled = K_orig.copy()
        K_scaled[0] *= W / W_orig   # fx, cx
        K_scaled[1] *= H / H_orig   # fy, cy

        # --- Point cloud ---
        xyz = _load_ply_xyz(s["ply_path"])

        # --- Depth map (full-res then resize) ---
        depth_full = _project_to_depth(
            xyz, s["pose_w2c"], K_orig, dist, W_orig, H_orig
        )
        # Resize with nearest to avoid interpolating depth across object edges
        depth_resized = cv2.resize(depth_full, (W, H), interpolation=cv2.INTER_NEAREST)
        depth = torch.from_numpy(depth_resized).unsqueeze(0)  # [1,H,W]
        mask = (depth > 0)  # [1,H,W]

        valid_frac = mask.float().mean().item()
        if valid_frac < self.min_valid_frac:
            log.debug("Sample %d has low coverage (%.3f) — will be used anyway", idx, valid_frac)

        return {
            "rgb": rgb,
            "depth": depth,
            "mask": mask,
            "session": s["session"],
            "cam": s["cam"],
            "frame": s["frame"],
            "valid_frac": valid_frac,
        }


# ---------------------------------------------------------------------------
# Quick validation / sanity check
# ---------------------------------------------------------------------------

def validate_one_sample(sessions_root: Optional[Path] = None, idx: int = 0) -> None:
    """Project one sample and save side-by-side PNG for visual inspection."""
    import matplotlib
    matplotlib.use("Agg")
    import matplotlib.pyplot as plt

    ds = UVeyePi3Dataset(sessions_root=sessions_root, max_samples=idx + 1)
    if len(ds) == 0:
        print("No samples found.")
        return

    sample = ds[idx]
    rgb = sample["rgb"].permute(1, 2, 0).numpy()
    depth = sample["depth"][0].numpy()
    mask = sample["mask"][0].numpy()

    print(f"Session: {sample['session']}  cam: {sample['cam']}  frame: {sample['frame']}")
    print(f"valid pixels: {mask.sum()}  ({sample['valid_frac']:.1%})")
    print(f"depth range: {depth[mask].min():.3f} – {depth[mask].max():.3f} m")

    fig, axes = plt.subplots(1, 3, figsize=(18, 5))
    axes[0].imshow(rgb)
    axes[0].set_title("RGB")
    d_vis = np.where(mask, depth, np.nan)
    im = axes[1].imshow(d_vis, cmap="plasma")
    plt.colorbar(im, ax=axes[1])
    axes[1].set_title("Depth (m)")
    axes[2].imshow(mask, cmap="gray")
    axes[2].set_title("Valid mask")
    for ax in axes:
        ax.axis("off")

    out = Path("/tmp/uveye_pi3_sanity_check.png")
    plt.tight_layout()
    plt.savefig(out, dpi=100)
    print(f"Saved: {out}")


if __name__ == "__main__":
    logging.basicConfig(level=logging.INFO)
    validate_one_sample()
